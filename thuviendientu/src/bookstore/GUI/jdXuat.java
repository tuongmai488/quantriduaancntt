    void binData(ArrayList<CTPX> lst) {
//        lst = nxbdll.getAll(t,w,o);
        Vector col = new Vector();
        col.add("Mã Sách");
        col.add("Số Lượng");
        col.add("Đơn Giá");
        col.add("Thành Tiền");

        Vector data = new Vector();
        for (CTPX i : lst) {
            Vector row = new Vector();
            row.add(i.getMaSach());
            row.add(i.getSoLuong());
            row.add(i.getDonGia());
            row.add(i.getThanhTien());
            data.add(row);
        }
        DefaultTableModel dtm = new DefaultTableModel(data, col);
        tbView.setModel(dtm);
    }

    void clearText() {
        cmbSach.setSelectedIndex(-1);
        spnSoLuong.setValue(1);
        cmbSach.requestFocus();
    }

    private void btnThemMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnThemMouseClicked
        // TODO add your handling code here:

        lsts = objs.getAll("", "", "");
        CTPX data = new CTPX();
        //        System.out.println(lsts.get(cmbSach.getSelectedIndex()).getMaSach());
        try {
            data.setMaSach(lsts.get(cmbSach.getSelectedIndex()).getMaSach());
            for (String i : lstMaSach) {
                if (i.equals(data.getMaSach())) {
                    JOptionPane.showMessageDialog(this, "Sách bạn chọn đã có!");
                    clearText();
                    return;
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Hãy lựa chọn sách!");
            clearText();
            return;
        }
        data.setSoLuong((int) spnSoLuong.getValue());
        if (data.getSoLuong() < 1) {
            JOptionPane.showMessageDialog(this, "Số lượng không hợp lệ!");
            spnSoLuong.setValue(1);
            return;
        }
        String w = "maSach = '" + data.getMaSach() + "'";
        lsts = objs.getAll("", w, "");
        if (data.getSoLuong() > lsts.get(0).getSoLuongTon()) {
            JOptionPane.showMessageDialog(this, "Sách trong kho còn " + lsts.get(0).getSoLuongTon() + " cuốn!", "Không đủ sách", 1);
            spnSoLuong.setValue(1);
            return;
        }
        data.setDonGia(lsts.get(0).getGiaBia());
        data.setThanhTien(data.getDonGia() * (data.getSoLuong()));
        lstMaSach.add(data.getMaSach());
        lstCTPX.add(data);
        //        System.out.println(lstCTPX.size() + " " + lstMaSach.size());
        binData(lstCTPX);
        clearText();
    }//GEN-LAST:event_btnThemMouseClicked

