    private void tbViewMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbViewMouseClicked
        // TODO add your handling code here:
        if (tbView.getSelectedRow() == -1) {
            return;
        }
        //        lsts = objs.getAll("", "", "");
        String item = tbView.getValueAt(tbView.getSelectedRow(), 0).toString();
        for (Sach i : lsts) {
            if (item.equals(i.getMaSach())) {
                item = i.getTieuDe();
                break;
            }
        }
        cmbSach.setSelectedItem(item);
        spnSoLuong.setValue(Integer.parseInt(tbView.getValueAt(tbView.getSelectedRow(), 1).toString()));
    }//GEN-LAST:event_tbViewMouseClicked

    private void btnXacNhanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXacNhanActionPerformed
        // TODO add your handling code here:
        if (lstCTPX.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Hóa đơn trống!");
            return;
        } else if (txtTenKH.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Hãy nhập tên khách hàng!");
            txtTenKH.requestFocus();
            return;
        } else if (isEdit) {
            PhieuXuat dt = new PhieuXuat();
            dt.setTenKH(txtTenKH.getText());
            String maPX = obj.lstEditctpx.get(0).getMaPhieuXuat();
            dt.setMaPhieuXuat(maPX);
            int thanhTien = 0;
            for (CTPX i : lstCTPX) {
                thanhTien += i.getThanhTien();
            }
            dt.setThanhTien(thanhTien);
            if (JOptionPane.showConfirmDialog(this, "Sửa bản ghi này?", "Thông báo!!!", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                if (objpx.updateData(dt)) {
                    for (CTPX x : lstCTPX) {
                        boolean check = true;
                        x.setMaPhieuXuat(maPX);
                        for (CTPX y : obj.lstEditctpx) {
                            if (x.getMaSach().equals(y.getMaSach())) {
                                check = false;
                                x.setMaCTPX(y.getMaCTPX());
                                //                            System.out.println(x.getMaCTPX() + " " + x.getMaPhieuXuat() + " " + x.getMaSach() + " " + x.getSoLuong() + " " + x.getDonGia() + " " + x.getThanhTien());
                                objctpx.updateData(x);
                                obj.lstEditctpx.remove(y);
                                break;
                            }
                        }
//                        System.out.println(check);
                        if (check) {
//                            System.out.println(obj.getIDCTPX());
                            x.setMaCTPX(obj.getIDCTPX());
                            objctpx.insertData(x);
                        }
                    }
                    for (CTPX y : obj.lstEditctpx) {
                        String id = y.getMaCTPX();
//                        System.out.println("del: " + y.getMaSach());
                        objctpx.deleteData_CTPX(id);
                    }
                    JOptionPane.showMessageDialog(this, "Sửa phiếu xuất thành công!");
                } else {
                    JOptionPane.showMessageDialog(this, "Sửa phiếu xuất thất bại!");
                }
            }

        } else {
            PhieuXuat data = new PhieuXuat();
            String idPX = obj.getIDPX();
            data.setMaPhieuXuat(idPX);
            data.setTenKH(txtTenKH.getText());
            //            DateTimeFormatter dtf = DateTimeFormatter.ofPattern("MM/dd/yyyy");
            LocalDate now = LocalDate.now();
            data.setNgayXuat(Date.valueOf(now));
            int thanhTien = 0;
            for (CTPX i : lstCTPX) {
                thanhTien += i.getThanhTien();
            }
            data.setThanhTien(thanhTien);
            if (JOptionPane.showConfirmDialog(this, "Tạo mới phiếu xuất?", "Thông báo!!!", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                if (objpx.insertData(data)) {
                    for (CTPX i : lstCTPX) {
                        //                    System.out.println(obj.getIDCTPX());
                        i.setMaCTPX(obj.getIDCTPX());
                        i.setMaPhieuXuat(idPX);
                        objctpx.insertData(i);
                    }
                    JOptionPane.showMessageDialog(this, "Tạo phiếu xuất thành công!");
                    int dialogResult = JOptionPane.showConfirmDialog(this, "Bạn muốn tạo hóa đơn?", "Thông báo", JOptionPane.YES_NO_OPTION);
                    if (dialogResult == JOptionPane.YES_OPTION) {
                        JFileChooser fc = new JFileChooser();
                        int option = fc.showSaveDialog(this);
                        if (option == JFileChooser.APPROVE_OPTION) {
                            String filename = fc.getSelectedFile().getName();
                            String path = fc.getSelectedFile().getParentFile().getPath();
                            int len = filename.length();
                            String ext = "";
                            String file = "";

                            if (len > 4) {
                                ext = filename.substring(len - 4, len);
                            }

                            if (ext.equals(".xls")) {
                                file = path + "\\" + filename;
                            } else {
                                file = path + "\\" + filename + ".xls";
                            }
                            try {
                                toExcel(data, tbView, new File(file));
                                if (JOptionPane.showConfirmDialog(this, "Mở file?", "Xuất file thành công!!!", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                                    File f = new File(file);
                                    try {
                                        Desktop.getDesktop().open(f);
                                    } catch (IOException e) {
                                        e.printStackTrace();
                                    }
                                }
                            } catch (IOException ex) {
                                Logger.getLogger(frmSach.class.getName()).log(Level.SEVERE, null, ex);
                                JOptionPane.showMessageDialog(this, "Xuất file không thành công!");
                            }
                        }
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Tạo phiếu xuất thất bại!");
                }
            }
        }
        this.setVisible(false);
    }//GEN-LAST:event_btnXacNhanActionPerformed

